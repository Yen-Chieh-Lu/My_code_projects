import sys


class ListNode:
    def __init__(self, data=0, pointer=None):
        self.val = data
        self.next = pointer


def add_2_numbers(l1: ListNode, l2: ListNode) -> ListNode:
    #######################
    #                     #
    #        TODO:        #
    #                     #
    #######################
    x = ListNode(0, None)
    cur = x
    carry = 0  # 進位
    while l1 or l2:
        if l1:
            v1 = l1.val
        else:
            v1 = 0
        if l2:
            v2 = l2.val
        else:
            v2 = 0
        val = v1 + v2 + carry
        carry = val // 10
        val = val % 10
        cur.next = ListNode(val, None)
        cur = cur.next
        l1 = l1.next if l1 else None
        l2 = l2.next if l2 else None
    if carry == 0:
        return x.next
    else:
        val = carry
        cur.next = ListNode(val, None)
        return x.next


